		if flag.Value then
			onTrue()
		else
			onFalse()
		end
	
		-- Bind listener
		local conn = flag:GetPropertyChangedSignal("Value"):Connect(function()
			if flag.Value then
				onTrue()
			else
				onFalse()
			end
		end)
	
		self.connections[flagName] = conn
	end
	
